package com.revature.methods;

public class Extra {
    private int age;
    private int num;

    public Extra(){}

    public Extra(int age, int num){
        this.age = age;
        this.num = num;
    }
    public int getAge(){
        return age;
    }
    public void setAge(int age) {this.age = age;}

    public int getNum(){
        return num;
    }
    public void setNum(int num){this.num = num;}
}
